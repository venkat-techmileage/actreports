package act.reports.model;

public class AccountRatePlanDetails {

	private String accountRatePlan = "";
	private String noOfTows = "";
	private String totalCharges = "";
	private String use = "";
	private String chargesPerTow = "";
	
	public String getAccountRatePlan() {
		return accountRatePlan;
	}
	public void setAccountRatePlan(String accountRatePlan) {
		this.accountRatePlan = accountRatePlan;
	}
	public String getNoOfTows() {
		return noOfTows;
	}
	public void setNoOfTows(String noOfTows) {
		this.noOfTows = noOfTows;
	}
	public String getTotalCharges() {
		return totalCharges;
	}
	public void setTotalCharges(String totalCharges) {
		this.totalCharges = totalCharges;
	}
	public String getUse() {
		return use;
	}
	public void setUse(String use) {
		this.use = use;
	}
	public String getChargesPerTow() {
		return chargesPerTow;
	}
	public void setChargesPerTow(String chargesPerTow) {
		this.chargesPerTow = chargesPerTow;
	}
}
