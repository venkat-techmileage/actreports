package act.reports.model;

public class DriverSalesDetails {
	
	private String totalTowingCharges = "";
	private String totalMileageCharges = "";
	private String totalLaborCharges = "";
	private String totalGateCharges = "";
	private String totalAdminCharges = "";
	private String subTotal = "";
	private String totalDiscounts = "";
	private String totalSales = "";
	private String totalInvoices = "";
	
	public String getTotalTowingCharges() {
		return totalTowingCharges;
	}
	public void setTotalTowingCharges(String totalTowingCharges) {
		this.totalTowingCharges = totalTowingCharges;
	}
	public String getTotalMileageCharges() {
		return totalMileageCharges;
	}
	public void setTotalMileageCharges(String totalMileageCharges) {
		this.totalMileageCharges = totalMileageCharges;
	}
	public String getTotalLaborCharges() {
		return totalLaborCharges;
	}
	public void setTotalLaborCharges(String totalLaborCharges) {
		this.totalLaborCharges = totalLaborCharges;
	}
	public String getTotalGateCharges() {
		return totalGateCharges;
	}
	public void setTotalGateCharges(String totalGateCharges) {
		this.totalGateCharges = totalGateCharges;
	}
	public String getTotalAdminCharges() {
		return totalAdminCharges;
	}
	public void setTotalAdminCharges(String totalAdminCharges) {
		this.totalAdminCharges = totalAdminCharges;
	}
	public String getSubTotal() {
		return subTotal;
	}
	public void setSubTotal(String subTotal) {
		this.subTotal = subTotal;
	}
	public String getTotalDiscounts() {
		return totalDiscounts;
	}
	public void setTotalDiscounts(String totalDiscounts) {
		this.totalDiscounts = totalDiscounts;
	}
	public String getTotalSales() {
		return totalSales;
	}
	public void setTotalSales(String totalSales) {
		this.totalSales = totalSales;
	}
	public String getTotalInvoices() {
		return totalInvoices;
	}
	public void setTotalInvoices(String totalInvoices) {
		this.totalInvoices = totalInvoices;
	}
}