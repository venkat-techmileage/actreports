package act.reports.model;

public class TowTypeSummaryDetails {

	private String towTypeSummary = "";
	private String total = "";
	private String charges = "";
	private String storage = "";
	private String commission = "";
	
	public String getTowTypeSummary() {
		return towTypeSummary;
	}
	public void setTowTypeSummary(String towTypeSummary) {
		this.towTypeSummary = towTypeSummary;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getCharges() {
		return charges;
	}
	public void setCharges(String charges) {
		this.charges = charges;
	}
	public String getStorage() {
		return storage;
	}
	public void setStorage(String storage) {
		this.storage = storage;
	}
	public String getCommission() {
		return commission;
	}
	public void setCommission(String commission) {
		this.commission = commission;
	}			
}
